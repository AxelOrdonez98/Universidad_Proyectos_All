    private void BotonGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonGenerarActionPerformed
        // TODO add your handling code here:
        double NuevoError, nuevoC = 0, nuevaX, C, x = 0;
        int Cont = 0;
        int decimales = (int) spinnerNumeroDeDecimales.getValue();
        C = Double.parseDouble(txtC.getText());
        nuevaX = Double.parseDouble(txtValorDeX.getText());
        if ((!txtC.getText().equals("")) || (!txtPorcentajeDeError.getText().equals("")) || (!txtValorDeX.getText().equals(""))) {
            do {
                Cont++;
                System.out.println("Numero de iteraciones: " + Cont);
                x = nuevaX;
                nuevoC = (.5) * (nuevaX + (C / nuevaX));
                nuevaX = nuevoC;
                BigDecimal db = new BigDecimal(nuevoC);
                db = db.setScale(decimales, RoundingMode.HALF_UP);
                System.out.println(db.doubleValue());
                NuevoError = (Math.abs(nuevoC - x) / x) * (100);
                BigDecimal db1 = new BigDecimal(NuevoError);
                db1 = db1.setScale(decimales, RoundingMode.HALF_UP);
                System.out.println("Su error relativo es de: " + db1.doubleValue());
                System.out.println("Er: " + NuevoError);
                nuevaX = nuevoC;
                System.out.println("---------------------------------------");
            } while (NuevoError >= Double.parseDouble(txtPorcentajeDeError.getText()));
            BigDecimal db = new BigDecimal(nuevoC);
            db = db.setScale(decimales, RoundingMode.HALF_UP);
            JOptionPane.showMessageDialog(null, "Raiz de " + C + ": "+db.doubleValue());
        } else {
            JOptionPane.showMessageDialog(null, "Por favor llene todo los campos");
        }

    }//GEN-LAST:event_BotonGenerarActionPerformed

